//java program to demonstrate unary operator
package Operators;

public class unary {
    public static void main(String[] args) {

        int num1 = 10;
        num1 += 5;

        int num2 = 10;
        num2 -= 5;

        int num3 =  10;
        num3 *= 3;

        int num4 = 10;
        num4 /= 2;

        int num5 = 10;
        num5 %= 2;

        System.out.println(num1);
        System.out.println(num2);
        System.out.println(num3);
        System.out.println(num4);
        System.out.println(num5);
    }
}
